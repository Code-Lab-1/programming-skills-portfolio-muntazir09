## Exercise 3: Stripping Names

#lstrip()

str_1 = '               Hello'
string_1 = str_1.lstrip()
print(str_1)
print(string_1)

#rstrip()

str_2 = 'How are you'
string_2 = str_2.rstrip('you')
print(str_2)
print(string_2)

#strip()

str_3 = 'This is a python program'
string_3 = str_3.strip('ro')
print(str_3)
print(string_3)

#\n

str_4 = 'Python is an \ninteresting language.'
print(str_4)

#\t

str_5 = 'Ayaz, \tDanish, \tUmer, \tTaha \tare my friends.'
print(str_5)


