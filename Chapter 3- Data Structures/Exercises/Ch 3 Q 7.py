## Exercise 7: Seeing the World

Touring = ['Canada ', 'Venezuela ', 'Turkey ', 'Scotland ', 'Japan ']
print(Touring)

# Using sorted()

print(sorted(Touring))
print(Touring)

# Using sorted() reverse

print(sorted(Touring, reverse = True))
print(Touring)

# Using reverse()

Touring.reverse()
print(Touring)

# Using reverse() again to bring it back to its original form.

Touring.reverse()
print(Touring)

# Sorting the list by using .sort()

Touring.sort()
print(Touring)

# Sorting the list by using .sort() in reverse

Touring.sort(reverse = True)
print(Touring)




